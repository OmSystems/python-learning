# String data type
name = 'Alice'
print(name, type(name))

# Boolean data type
is_student = True
print(is_student, type(is_student))

# Integer data type
age = 20
print(age, type(age))

# Float data type
score = 80.5
print(isinstance(score, float))
print(score, type(score))
